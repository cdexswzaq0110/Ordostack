export function resolveLocale(value: string | null): LocaleCode {
  return value === "zh-TW" ? "zh-TW" : DEFAULT_LOCALE;
}

export function translate(locale: LocaleCode, key: string): string {
  if (locale === "zh-TW") {
    return zhTW[key] ?? key;
  }
  return key;
}
